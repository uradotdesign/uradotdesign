/**
 * Scans all Directus content for CSS classes used in admin-authored HTML
 * (rich text, custom_code, Custom Code blocks, embeds) and writes them to
 * `src/styles/cms-classes.generated.html`, which Tailwind v4 scans via an
 * `@source` directive in `globals.css`.
 *
 * Tailwind only emits CSS for classes it sees in scanned source files; CMS
 * content lives in the database, so without this step any class typed into the
 * CMS produces no CSS. Pair this with the curated safelist in `globals.css`
 * (instant, common utilities) — this scanner backfills the long tail of classes
 * actually used in content so they are baked into the next deploy.
 *
 * Runs in `prebuild`. If no admin credentials are present (e.g. a build host
 * without `.env`), it warns and exits 0, leaving the committed generated file
 * intact so the build never breaks.
 *
 * Usage:
 *   node --env-file=.env scripts/scan-cms-tailwind.mjs
 *   npm run scan:cms
 */
import { writeFileSync, readFileSync, existsSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const OUT = resolve(
  dirname(fileURLToPath(import.meta.url)),
  "../src/styles/cms-classes.generated.html"
);

// class="..." or class='...' anywhere in a string value.
const CLASS_ATTR = /class\s*=\s*("([^"]*)"|'([^']*)')/gi;
// Plausible utility token charset (covers arbitrary values like bg-[var(--x)]).
const TOKEN_OK = /^[A-Za-z0-9_:/.,%#\\[\]()!-]+$/;

function extractClasses(value, sink) {
  if (typeof value !== "string" || !value.includes("class")) return;
  let m;
  while ((m = CLASS_ATTR.exec(value)) !== null) {
    const raw = m[2] ?? m[3] ?? "";
    for (const tok of raw.split(/\s+/)) {
      if (tok && tok.length <= 80 && TOKEN_OK.test(tok)) sink.add(tok);
    }
  }
}

function walk(node, sink) {
  if (node == null) return;
  if (typeof node === "string") return extractClasses(node, sink);
  if (Array.isArray(node)) {
    for (const v of node) walk(v, sink);
    return;
  }
  if (typeof node === "object") {
    for (const v of Object.values(node)) walk(v, sink);
  }
}

function writeOutput(classes) {
  const sorted = [...classes].sort();
  const body = sorted.length
    ? sorted.map((c) => `<div class="${c}"></div>`).join("\n")
    : "<!-- no CMS classes found -->";
  const header =
    "<!-- AUTO-GENERATED by scripts/scan-cms-tailwind.mjs. Do not edit by hand.\n" +
    "     Tailwind scans this (see @source in globals.css) so classes used in\n" +
    `     CMS content compile into the build. ${sorted.length} classes. -->\n`;
  const next = header + body + "\n";
  const prev = existsSync(OUT) ? readFileSync(OUT, "utf8") : "";
  if (prev === next) {
    console.log(`No change. ${sorted.length} classes in ${OUT}`);
    return;
  }
  writeFileSync(OUT, next);
  console.log(`Wrote ${sorted.length} classes -> ${OUT}`);
}

async function main() {
  let admin;
  try {
    const { createDirectusAdmin } = await import("./lib/directus-admin.mjs");
    admin = createDirectusAdmin();
  } catch (e) {
    console.warn(
      `! Skipping CMS class scan (${e.message}); keeping existing generated file.`
    );
    process.exit(0);
  }
  const { authRequest, baseUrl } = admin;
  console.log(`\nScanning CMS content for Tailwind classes -> ${baseUrl}\n`);

  const cols = (await authRequest("/collections?limit=-1"))?.data ?? [];
  const targets = cols
    .map((c) => c.collection)
    .filter((n) => n && !n.startsWith("directus_"))
    .sort();

  const classes = new Set();
  let scanned = 0;
  for (const col of targets) {
    try {
      const res = await authRequest(
        `/items/${encodeURIComponent(col)}?limit=-1&fields=*`
      );
      const data = res?.data;
      const items = Array.isArray(data) ? data : data ? [data] : [];
      for (const item of items) walk(item, classes);
      scanned += items.length;
    } catch (e) {
      // Some collections (e.g. system folders) won't be readable as items.
      if (e.status !== 403 && e.status !== 404) {
        console.warn(`! ${col}: ${e.message}`);
      }
    }
  }

  console.log(
    `Scanned ${scanned} rows across ${targets.length} collections; ` +
      `found ${classes.size} unique classes.`
  );
  writeOutput(classes);
}

main().catch((e) => {
  console.error(e?.message || e);
  process.exit(1);
});
